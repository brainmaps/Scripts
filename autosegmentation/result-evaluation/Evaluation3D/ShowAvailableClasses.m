function mainHandle = ShowAvailableClasses(availableClasses)

% Read resolution
scrsz = get(0,'ScreenSize');

% Define window position and size
winSize = [150 100];
winPosition = [scrsz(3)/2 - winSize(1)/2, scrsz(4)/2 - winSize(2)/2, winSize(1), winSize(2)];

% Create window
handles.MainWindow = figure( ...
    'MenuBar', 'none', ...
    'Name', 'Available classes', ...
    'NumberTitle', 'off', ...
    'ToolBar', 'none', ...
    'Position', winPosition, ...
    'Units', 'pixels', ...
    'Color', get(0, 'defaultuicontrolbackgroundcolor'));

%%  Add Controls

handles.textAvailableClasses = uicontrol(handles.MainWindow, ...
    'Tag', 'textAvailableClasses', ...
    'Units', 'normalized', ...
    'FontUnits', 'normalized', ...
    'FontSize', .15, ...
    'HorizontalAlignment', 'left', ...
    'Position', [0.02 0.02 .96 .96], ...
    'Style', 'text', ...
    'String', 'test');

%% Create Text

outString = [];
for i = 1:length(availableClasses)
    outString = sprintf([outString num2str(i) ') ' availableClasses{i} '\n']);
end

set(handles.textAvailableClasses, 'String', outString);

%% Output

mainHandle = handles.MainWindow;

end
